#include "3-calc.h"
/**
 * op_add - integer
 * @a: integer
 * @b: integer
 * Return: a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - integer
 * @a: integer
 * @b: intger
 * Return: a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - integer
 * @a: integer
 * @b: integer
 * Return: a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - integer
 * @a: integer
 * @b: integer
 * Return: a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - integer
 * @a: integer
 * @b: integer
 * Return: a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
